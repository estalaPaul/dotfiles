local function detect_indent(buf)
  -- Don't override if filetype sets its own (optional)
  -- if vim.bo[buf].filetype == "make" then return end

  local line_count = vim.api.nvim_buf_line_count(buf)
  local max_lines = math.min(256, line_count)
  local lines = vim.api.nvim_buf_get_lines(buf, 0, max_lines, false)

  local has_tab = false
  local c2, c4 = 0, 0

  for _, line in ipairs(lines) do
    -- count leading spaces (ignore blank/whitespace-only lines)
    local s = line:match("^%s+")
    if s and not line:match("^%s*$") then
      local n = #s
      if n % 2 == 0 then c2 = c2 + 1 end
      if n % 4 == 0 then c4 = c4 + 1 end
    end
  end

  local sw = (c2 > c4) and 2 or 4
  vim.bo[buf].expandtab = true
  vim.bo[buf].tabstop = sw
  vim.bo[buf].shiftwidth = sw
  vim.bo[buf].softtabstop = sw
end

local grp = vim.api.nvim_create_augroup("DetectIndentLua", { clear = true })
vim.api.nvim_create_autocmd({ "BufReadPost", "BufNewFile" }, {
  group = grp,
  callback = function(args) detect_indent(args.buf) end,
})
